# min(), max(), sum() functions of list and tuple

myList=[9, 45, 6, 75, 12, 4.5]

minValueList=min(myList)
print(f'The min value from the list is: {minValueList}')

maxValList=max(myList)
print(f'The max value from the list is: {maxValList}')

SumList=sum(myList)
print(f'The sum of all items in the list are: {SumList}')
print()

# Number Data Type - integer, float and complex number
x=5; y=6.458; z=5+4j
print(x,y,z)
print(type(x),type(y),type(z))
print()

# String Data Type
# string is a single character, multiple character or group of words
# its very important to define the strings in quotes, either single, double or tripple, otherwise face errors
l='a'; m='apple'; n="Apple iphone 16"
print(l,m,n)
print(type(l),type(m),type(n))
print()

# Boolean Data Type
# are True and False, dont provide quotes else they become strings
myBool1=True
myBool2=False
myBool11='True'
print(myBool11,type(myBool11))  # Observe this output, with quotes your data type changes to string
print(myBool1,type(myBool1),myBool2,type(myBool2))
print()

# None Data Type
MynoneData=None     # Dont use quotes, else it becomes string
print(MynoneData, type(MynoneData))


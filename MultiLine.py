# Single line input - multiple line output, use \n
myStr='Docker helps developers bring their ideas to life \nby conquering the complexity of app development. \nActively used by millions of developers around the world, \nDocker Desktop and Docker Hub provide unmatched simplicity, agility and choice.'
print(myStr)
print()

# multiple line input - Single line output, use \ to seperate each line
myStr='Docker helps developers bring their ideas to life \
by conquering the complexity of app development. \
Actively used by millions of developers around the world, \
Docker Desktop and Docker Hub provide unmatched simplicity, agility and choice.'
print(myStr)
print()

# multiple line input - muliple line output, use ''' or """  to quote multiple lines and avoid using \ as a seperator
myStr='''Docker helps developers bring their ideas to life 
by conquering the complexity of app development.
Actively used by millions of developers around the world, 
Docker Desktop and Docker Hub provide unmatched simplicity, agility and choice.'''
print(myStr)
print()